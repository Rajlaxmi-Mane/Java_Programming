class DoubleConv
{
	public static void main(String[] args) 
	{
	    double i=265.897d;
		byte b=(byte)i;
		short s=(short)i;
		
		int l=(int)i;

		long f=(long)i;
		float d=(float)i;
		char c=(char)i;

		 System.out.println(i);
		 System.out.println(b);
		 System.out.println(s);
		 System.out.println(l);
		 System.out.println(f);
		 System.out.println(d);
		 System.out.println(c);
	}
}

